# Definition for a point.
class Point(object):
    def __init__(self, a=0, b=0):
        self.x = a
        self.y = b

class Solution(object):
    def maxPoints(self, points):
        """
        :type points: List[Point]
        :rtype: int
        """
        import sys
        if not points: return 0
        elif len(points)==1: return 1
        n = len(points)
        tmp = {}
        for i in range(n):
            for j in range(i+1,n):
                p,q=points[i],points[j]
                if p.x !=q.x:
                    k=float(p.y-q.y)/(p.x-q.x)
                    b=p.y-k*(p.x)
                else:
                    k=sys.maxint
                    b=p.x
                #if abs(k-0.4142857142857143)<0.0001 and abs(b-15.999999999999972)<0.0001:
                #    print p.x,p.y,q.x,q.y
                if (k,b) not in tmp: tmp[(k,b)]=set([p,q])
                else: tmp[(k,b)] |=set([p,q])
        
        
        #for k,v in tmp.iteritems():
        #    print "key",k,
        #    for t in v:
        #        print "point",t.x,t.y,
        #    print
        
        res=0
        for k,v in tmp.iteritems():
            res=max(res,len(v))
        return res

